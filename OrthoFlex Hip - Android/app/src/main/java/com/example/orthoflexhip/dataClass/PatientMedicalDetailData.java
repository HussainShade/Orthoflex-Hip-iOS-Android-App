package com.example.orthoflexhip.dataClass;

public class PatientMedicalDetailData {
    private String id;
    private String username;
    private String password;
    private String name;
    private String age;
    private String gender;
    private String mobile;
    private String hospital_id;
    private String height;
    private String weight;
    private String problem;
    private String admitted_date;
    private String discharge_date;
    private String feedback;
    private String score;
    private String score_result;

    public String getID() { return id; }
    public void setID(String value) { this.id = value; }

    public String getUsername() { return username; }
    public void setUsername(String value) { this.username = value; }

    public String getPassword() { return password; }
    public void setPassword(String value) { this.password = value; }

    public String getName() { return name; }
    public void setName(String value) { this.name = value; }

    public String getAge() { return age; }
    public void setAge(String value) { this.age = value; }

    public String getGender() { return gender; }
    public void setGender(String value) { this.gender = value; }

    public String getMobile() { return mobile; }
    public void setMobile(String value) { this.mobile = value; }

    public String getHospitalID() { return hospital_id; }
    public void setHospitalID(String value) { this.hospital_id = value; }

    public String getHeight() { return height; }
    public void setHeight(String value) { this.height = value; }

    public String getWeight() { return weight; }
    public void setWeight(String value) { this.weight = value; }

    public String getProblem() { return problem; }
    public void setProblem(String value) { this.problem = value; }

    public String getAdmittedDate() { return admitted_date; }
    public void setAdmittedDate(String value) { this.admitted_date = value; }

    public String getDischargeDate() { return discharge_date; }
    public void setDischargeDate(String value) { this.discharge_date = value; }

    public String getFeedback() { return feedback; }
    public void setFeedback(String value) { this.feedback = value; }

    public String getScore() { return score; }
    public void setScore(String value) { this.score = value; }

    public String getScoreResult() { return score_result; }
    public void setScoreResult(String value) { this.score_result = value; }
}
